import java.util.Scanner;

public class Main{
  public static void main(String [] arg) {
	  int a,b;
	  String c;
	  System.out.print("두 정수와 연산자를 입력하시오>>");
	  Scanner scanner = new Scanner(System.in);
	  a = scanner.nextInt();
	  b= scanner.nextInt();
	  c = scanner.next();
	  switch(c)
	  {
      case "+": 
          Add add = new Add();
          add.setValue(a, b);
          System.out.println(add.calculate());
          break;
      case "-":
    	 Sub sub = new Sub();
          sub.setValue(a, b);
          System.out.println(sub.calculate());
          break;
      case "*" :
    	  Mul mul = new Mul();
    	  mul.setValue(a, b);
    	  System.out.println(mul.calculate());
          break;
      case "/" :
    	  Div div = new Div();
    	  div.setValue(a, b);
    	  System.out.println( div.calculate());
          break;
      default :
          System.out.println("해당하는 연산이 없습니다.");
	  }
	  scanner.close();
  }
}
class Add{
  int a, b;
  void setValue(int a, int b) {
      this.a = a;
      this.b= b;
  }
  int calculate() {
     return a+b;
  }
}
class Sub{
  int a, b;
  void setValue(int a, int b) {
	  this.a = a;
      this.b= b;
  }
  int calculate() {
      return a-b;
  }
}
class Mul{
  int a, b;
  void setValue(int a, int b) {
	  this.a = a;
      this.b= b;
  }
  int calculate() {
	  return a*b;
  }
}
class Div{
  int a, b;
  void setValue(int a, int b) {
	  this.a = a;
      this.b= b;
  }
  int calculate() {
      return a/b;
  }
}